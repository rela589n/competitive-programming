// 1742 G. Orray (OR array)
// _bitmasks_, _brute_, _greedy_, _math_, *1500

fscanf(STDIN, '%u', $t);

while ($t--) {
    fscanf(STDIN, '%u', $n);

    $ar = fscanf(STDIN, $arForm = str_repeat('%u ', $n));

    $vis = array_fill(0, $n, false);
    $res = [];
    $curOr = 0;

    for ($i = 0; $i < min($n, 31); ++$i) {
        $maxOr = $curOr;
        $maxJ = -1;

        for ($j = 0; $j < $n; ++$j) {
            if ($vis[$j]) {
                continue;
            }

            $newOr = $curOr | $ar[$j];

            if ($newOr >= $maxOr) {
                $maxOr = $newOr;
                $maxJ = $j;
            }
        }

        $res [] = $ar[$maxJ];
        $vis[$maxJ] = true;
        $curOr = $maxOr;
    }

    foreach ($ar as $j => $a) {
        if (!$vis[$j]) {
            $res[] = $a;
        }
    }

    fprintf(STDOUT, $arForm.PHP_EOL, ...$res);
}

